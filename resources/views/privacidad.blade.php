    </li>
</ul>
<h2>Datos recopilados</h2>
<p>Recopilamos varios tipos de información con diferentes fines para proporcionar y mejorar nuestro Servicio.</p>
<h3>Tipos de datos recopilados</h3>
<h4>Datos personales</h4>
<p>Al utilizar nuestro Servicio, es posible que se le solicite que nos proporcione cierta información de
    identificación personal que pueda ser utilizada para contactarlo o identificarlo ("Datos personales"). La
    información de identificación personal puede incluir, pero no se limita a:</p>
<ul>
    <li>Dirección de correo electrónico</li>
    <li>Nombre y apellidos</li>
    <li>Número de teléfono</li>
    <li>Dirección, ciudad, estado, provincia, código postal</li>
    <li>Cookies y datos de uso</li>
</ul>
<h4>Datos de uso</h4>
<p>También podemos recopilar información sobre cómo se accede y utiliza nuestro Servicio ("Datos de uso"). Estos
    Datos de uso pueden incluir información como la dirección del protocolo de Internet de su computadora (por
    ejemplo, dirección IP), tipo de navegador, versión del navegador, las páginas de nuestro Servicio que visita, la
    hora y la fecha de su visita, el tiempo dedicado a esas páginas, identificadores de dispositivos únicos y otros
    datos de diagnóstico.</p>
<h4>Datos de cookies y seguimiento</h4>
<p>Utilizamos cookies y tecnologías de seguimiento similares para rastrear la actividad en nuestro Servicio y
    mantener cierta información. Las cookies son archivos con una pequeña cantidad de datos que pueden incluir un
    identificador único anónimo. Las cookies se envían a su navegador desde un sitio web y se almacenan en su
    dispositivo. Otras tecnologías de seguimiento también utilizadas son balizas, etiquetas y scripts para recopilar
    y rastrear la información, así como para mejorar y analizar nuestro Servicio.</p>
<h2>Uso de datos</h2>
<p>Aeroweb utiliza los datos recopilados con diversas finalidades:</p>
<ul>
    <li>Para proporcionar y mantener nuestro Servicio</li>
    <li>Para notificarle sobre cambios en nuestro Servicio</li>
    <li>Para permitirle participar en funciones interactivas de nuestro Servicio cuando decida hacerlo</li>
    <li>Para proporcionar asistencia al cliente</li>
    <li>Para recopilar análisis o información valiosa que nos permita mejorar nuestro Servicio</li>
    <li>Para controlar el uso de nuestro Servicio</li>
    <li>Para detectar, prevenir y abordar problemas técnicos</li>
</ul>
<h2>Transferencia de datos</h2>
<p>Su información, incluidos los Datos personales, puede transferirse a, y mantenerse en, computadoras ubicadas
    fuera de su estado, provincia, país u otra jurisdicción gubernamental donde las leyes de protección de datos
    pueden diferir de las de su jurisdicción.</p>
<h2>Divulgación de datos</h2>
<p>Aeroweb puede divulgar sus Datos personales si cree de buena fe que dicha acción es necesaria para:</p>
<ul>
    <li>Cumplir con una obligación legal</li>
    <li>Proteger y defender los derechos o propiedades de Aeroweb</li>
    <li>Prevenir o investigar posibles infracciones en relación con el Servicio</li>
    <li>Proteger la seguridad personal de usuarios del Servicio o del público</li>
    <li>Protegerse contra la responsabilidad legal</li>
</ul>
<h2>Seguridad de datos</h2>
<p>La seguridad de sus datos es importante para nosotros, pero recuerde que ningún método de transmisión por
    Internet o método de almacenamiento electrónico es 100% seguro. Si bien nos esforzamos por utilizar medios
    comercialmente aceptables para proteger sus Datos personales, no podemos garantizar su seguridad absoluta.</p>
<h2>Proveedores de servicios</h2>
<p>Podemos emplear a terceras compañías y particulares para facilitar nuestro Servicio ("Proveedores de servicios"),
    para proporcionar el Servicio en nuestro nombre, para realizar servicios relacionados con el Servicio o para
    ayudarnos a analizar cómo se utiliza nuestro Servicio.</p>
<h2>Enlaces a otros sitios</h2>
<p>Nuestro Servicio puede contener enlaces a otros sitios que no son operados por nosotros. Si hace clic en el
    enlace de un tercero, será dirigido al sitio de ese tercero. Le recomendamos encarecidamente que revise la
    Política de privacidad de todos los sitios que visite.</p>
<h2>Cambios en esta Política de privacidad</h2>
<p>Podemos actualizar nuestra Política de privacidad de vez en cuando. Le notificaremos cualquier cambio publicando
    la nueva Política de privacidad en esta página.</p>
<p>Le informaremos por correo electrónico y / o un aviso destacado sobre nuestro Servicio antes de que el cambio
    entre en vigencia y actualizaremos la "fecha de vigencia" en la parte superior de esta Política de
    privacidad.</p>
<p>Se le recomienda que revise esta Política de privacidad periódicamente para ver si hay cambios. Los cambios en
    esta Política de privacidad entran en vigencia cuando se publican en esta página.</p>
