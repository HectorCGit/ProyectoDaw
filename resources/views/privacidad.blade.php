<h1>Política de privacidad</h1>
<p>Última actualización: 5 de junio de 2023</p>
<p>Esta Política de privacidad describe nuestras políticas y procedimientos sobre la recopilación, uso y divulgación de
    su información cuando utiliza el Servicio, y le informa sobre sus derechos de privacidad y cómo la ley lo
    protege.</p>
<p>Utilizamos sus datos personales para proporcionar y mejorar el Servicio. Al utilizar el Servicio, usted acepta la
    recopilación y el uso de información de acuerdo con esta Política de privacidad. </p>
<h1>Interpretación y definiciones</h1>
<h2>Interpretación</h2>
<p>Las palabras cuya inicial está en mayúscula tienen significados definidos en las siguientes condiciones. Las
    siguientes definiciones tendrán el mismo significado independientemente de si aparecen en singular o en plural.</p>
<h2>Definiciones</h2>
<p>A los efectos de esta Política de privacidad:</p>
<ul>
    <li>
        <p><strong>Cuenta</strong> significa una cuenta única creada para que usted acceda a nuestro Servicio o partes
            de nuestro Servicio.</p>
    </li>
    <li>
        <p><strong>Afiliado</strong> significa una entidad que controla, es controlada por o está bajo control común con
            una parte, donde "control" significa la propiedad del 50% o más de las acciones, intereses de capital u
            otros valores con derecho a voto para la elección de directores u otra autoridad de gestión.</p>
    </li>
    <li>
        <p><strong>Compañía</strong> (referida como "la Compañía", "Nosotros", "Nuestro" en este Acuerdo) se refiere a
            Aeroweb.</p>
    </li>
    <li>
        <p><strong>Cookies</strong> son archivos pequeños que se colocan en su computadora, dispositivo móvil o
            cualquier otro dispositivo por un sitio web, que contienen los detalles de su historial de navegación en ese
            sitio web, entre sus muchos usos.</p>
    </li>
    <li>
        <p><strong>País</strong> se refiere a: España</p>
    </li>
    <li>
        <p><strong>Dispositivo</strong> significa cualquier dispositivo que pueda acceder al Servicio, como una
            computadora, un teléfono celular o una tableta digital.</p>
    </li>
